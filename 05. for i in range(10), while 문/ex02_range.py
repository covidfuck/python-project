for a in range(10): # range(end)  0 ~ 9
    print(f"a : {a}")
print("==============================")
for b in range(1, 10): # range(start, end) 1 ~ 9
    print(f"b : {b}")
print("==============================")
for c in range(1, 10, 2): # range(start, end, step) 1 ~ 9 사이의 간격 2 (1 3 5 7 9)
    print(f"c : {c}")