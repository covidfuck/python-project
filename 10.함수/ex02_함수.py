# gugudan 함수 만들기
# 숫자를 넣으면 해당 구구단이 출력

def gugudan():
    a = int(input('몇단을 출력할까요? : '))
    print(f'--------------------- {a}단 ---------------------')
    for i in range(1, 10):
        print(f'{a} x {i} = {a * i}')

gugudan()



